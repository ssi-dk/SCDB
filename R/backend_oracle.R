#' @importMethodsFrom DBI dbWriteTable
#' @importMethodsFrom RJDBC dbWriteTable
#' @exportMethod dbWriteTable
setMethod(
  "dbWriteTable",
  signature(
    conn = "OracleJdbc",
    name = "character",
    value = "data.frame"
  ),
  function(conn, name, value, ...) {
    DBI::dbWriteTable(conn@jdbc_conn, id(conn@jdbc_conn, name), value, ...)
  }
)

#' @importMethodsFrom DBI dbWriteTable
