#' @exportMethod dbGetInfo
setMethod(
  "dbGetInfo",
  signature(
    dbObj = "OracleJdbc"
  ),
  function(dbObj, ...) {
    modifyList(
      DBI::dbGetInfo(dbObj@jdbc_conn, ...),
      list(
        "servername" = dbObj@servername,
        "port" = ""
      )
    )
  }
)

