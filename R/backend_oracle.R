  "dbWriteTable",
  signature(
    conn = "OracleJdbc",
    name = "SQL",
    value = "data.frame"
  ),
  function(conn, name, value, ...) {

    names(value) <- as.character(DBI::dbQuoteIdentifier(conn@jdbc_conn, names(value)))

    DBI::dbWriteTable(conn@jdbc_conn, name, value)
  }
)


